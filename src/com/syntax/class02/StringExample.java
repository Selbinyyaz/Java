package com.syntax.class02;

public class StringExample {

	public static void main(String[] args) {
		
		String name = "Selbi";
		String lastName = "Altyyeva";
		char grade = 'B';
		String city = "Little Rock";
		String state = "Arkansas";
		long phoneNumber = 123456789;
		
		city = "Houston";
		state = "Texas";
		phoneNumber = 1234322344;
		grade = 'A';
		
		System.out.println(name);
		System.out.println(lastName);
		System.out.println(grade);
		System.out.println(city);
		System.out.println(state);
		System.out.println(phoneNumber);
		
		System.out.println("My name is "+ name + " and my lastname is "+lastName);
		System.out.println("I live in city of "+city+" in state "+state+" and my phone number is "+phoneNumber);
		
		
	
	}

}
