package com.syntax.class07;

public class HW {

	public static void main(String[] args) {
		// Print numbers from 1 to 100 in 1 line with space
		int num1 = 1;
		while (num1 <= 100) {
			System.out.print(num1+ " ");
			num1++;

		}
		//System.out.println("---------------------------");
		// Print numbers from 100 to 1
		int num2 = 101;
		while (num2 >= 1) {
			System.out.println(num2);
			num2--;
		}
		

	}

}
